#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			j = s1[i] - s2[i];
			return (j);
		}
		i++;
	}
	if (s1[i] == s2[i] && s2[i] == '\0')
		return (0);
	return (j);
}
